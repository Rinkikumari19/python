def calculator(number_x,number_y,operation):
    if operation == "add":
        print(number_x + number_y)
    elif operation == "subtract":
        print(number_x - number_y)
    elif operation == "multiple":
        print(number_x * number_y)
    elif operation == "divide":
        print(number_x % number_y)
number_1 = calculator(24,20,"add")
